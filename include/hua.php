function add_rubish($str){
    $len = strlen($str)*2;

    $i=1;
    while($i<$len){
        $str = substr_replace($str,'?',$i,0);
        $i=$i+2;
    }
    return $str;
}

function del_rubish($str){
    return str_replace('?','',$str);
}
